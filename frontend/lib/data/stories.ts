export function getStoryById(id: string): Story | undefined {
  return seedStories.find((story) => story.id === id);
}

export function filterStories(
  filters?: { categories?: string[]; search?: string }
): Story[] {
  let filtered = [...seedStories];

  if (filters?.categories && filters.categories.length > 0) {
    filtered = filtered.filter((story) =>
      story.tags.some((tag) => filters.categories?.includes(tag))
    );
  }

  if (filters?.search) {
    const searchLower = filters.search.toLowerCase();
    filtered = filtered.filter(
      (story) =>
        story.title.toLowerCase().includes(searchLower) ||
        story.excerpt.toLowerCase().includes(searchLower) ||
        story.tags.some((tag) => tag.toLowerCase().includes(searchLower))
    );
  }

  return filtered;
}
